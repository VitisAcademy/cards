function take_answer(section, coin) {
    /**
     section: Позиция ответа в DOM.
     coin: Количество баллов за ответ.

     action:: Подсчитывает баллы; показывает правильный ответ.
     setTimeout(1s):: Возвращает прежний цвет после анимации.
    **/
    block_tab();
    counter += coin;
    let line = section.parentElement.querySelectorAll('.answer__line');
    if (coin === 1) {
        line[0].style.backgroundColor = '#3FA02F';
        line[1].style.backgroundColor = '#3FA02F';
    }
    else {
        line[0].style.backgroundColor = '#C42323';
        line[1].style.backgroundColor = '#C42323';
    }
    function line_def(){
        line[0].style.backgroundColor = '#D9D9D9';
        line[1].style.backgroundColor = '#D9D9D9';
    }
    window.setTimeout(line_def, 1000);

    theme[card].children[0].classList.remove('spin__back');  // task__front
    theme[card].children[1].classList.remove('hidden');  // task__back
    theme[card].children[0].classList.add('swap__front');  // task__front
    theme[card].children[1].classList.add('swap__back');  // task__back

    function task_hidden(){
        theme[card].children[0].classList.add('hidden');  // task__front
    }
    window.setTimeout(task_hidden, 1000);
}

