function shuffle(arr){
    /**
     arr: Массив.
     type: {object}
     return: Случайно переупорядоченный массив.
    **/

    return arr.sort(() => Math.random() - 0.5);
}
function in_quests(IN){
    /**
     IN: База вопросов для INPUT.
     type: {object}
     return: Массив с HTML формой вопроса.
    **/

    let INQuests = [];  // сюда будут генерироваться HTML
    for (let i in IN){  // длинна массива равна длинне IN
        INQuests.push(`<div class="task">
        <div class="task__front wrapper hidden">
        <div class="task__question">${IN[i].question}</div>
        <div class="task__answer task__input">
        <input type="text" maxlength="30" placeholder="Введите ответ сюда" autocomplete="off" onchange="on_change(this.value)" autofocus class="input__answer">
        <div class="answer__line input__line"></div>
        <div class="answer__line input__line" style="display: none"></div></div>
        <div class="input__button button" onclick="input_answer(this, '${IN[i].answers}')">ОТПРАВИТЬ</div></div>
        <div class="task__back wrapper hidden">
        <div class="task__bg">
        <div class="task__about">${IN[i].about}</div>
        <div class="task__button button" onclick="go_next()">ПРОДОЛЖИТЬ</div></div></div></div>`)
    }

    return INQuests;
}
function q_answers(Q){
    /**
     Q: База вопросов для Q.
     type: {object}
     return: Массив с массивом ответов.

     QAnswers = [ [answers], [answers], [answers] ].
     'длинна массива' = 'длинна ответов' :: 'длинна вопросов'.
     **/

    let QAnswers = []; // сюда будут генерироваться HTML
    for (let i in Q){  // длинна массива равна длинне Q
        let answers = Q[i].answers;
        let correct = Q[i].correct;
        let set = [];
        for (let j in answers){
            set.push(`<div class="answer" onclick="take_answer(this, ${correct[j]})">${answers[j]}</div>
            <div class="answer__line"></div>`);
        }
        shuffle(set);
        QAnswers.push(set);
    }

    return QAnswers;
}
