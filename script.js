function on_change(value){
    /**
     value: Содержимое элемента.
     type: {string}

     action:: Сохраняет value, при изменении последнего.
    **/
    input_val = value.toLowerCase();
}
function input_answer(section, answers){
    /**
     section: Позиция ответа в DOM.
     type: {HTMLElement}
     answers: Строка ответов на вопрос.
     type: {string}

     action:: Проверяет введённый ответ; отвечает на вопрос.
     setTimeout(1s):: Возвращает прежний цвет после анимации; скрывает карту после анимации.
    **/

    block_tab();
    answers = answers.split(',');
    let color = section.previousElementSibling.children[1];  // input__line
    let coin = 0;
    for (let i in answers){
        if (input_val === answers[i]) {
            counter += 1;
            coin += 1;
        }
    }
    if (coin === 1) {
        color.style.backgroundColor = '#3FA02F';
    } else {
        color.style.backgroundColor = '#C42323';
    }

    function line_def(){
        color.style.backgroundColor = '#D9D9D9';
    }
    window.setTimeout(line_def, 1000);

    theme[card].children[0].classList.remove('spin__back');  // task__front
    theme[card].children[1].classList.remove('hidden');  // task__back
    theme[card].children[0].classList.add('swap__front');  // task__front
    theme[card].children[1].classList.add('swap__back');  // task__back

    function task_hidden(){
        theme[card].children[0].classList.add('hidden');  // task__front
    }
    window.setTimeout(task_hidden, 1000);
}

